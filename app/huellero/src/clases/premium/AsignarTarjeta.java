    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fila = tablaAlumnos.getSelectedRow();
        if (fila > -1) {
            String id = (String) tablaAlumnos.getValueAt(fila, 0);
            String Datos = (String) tablaAlumnos.getValueAt(fila, 1);
            String saldo = (String) tablaAlumnos.getValueAt(fila, 3);
            String token = (String) tablaAlumnos.getValueAt(fila, 2);
            txtcodigo.setText(id);
            txtsaldo.setText(saldo);
            txtdatos.setText(Datos);
            txttoken.setText(token);
            // btnguardar.setEnabled(true);
            btnguardar1.setEnabled(true);
            pantalla.seleccionado = true;
        } else {
            JOptionPane.showMessageDialog(rootPane, "PorFavor Seleccione un Alumno");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
