    public void obtenerAlumnos(String bus) {
        Vector VAlumnos = pantalla.conexion.datosAlumnos(bus);
        Alumnos = new mAlumno[VAlumnos.size()];
        limpiartabla();
        for (int i = 0; i < VAlumnos.size(); i++) {
            Alumno = (mAlumno) VAlumnos.get(i);
            llenartabla(Alumno);
            // System.out.println(Alumno.getDatos());;
            Alumnos[i] = Alumno;
        }

    }

    public void limpiar() {
        txtsaldo.setText("----------------------------");
        txtdatos.setText("----------------------------");
        txttoken.setText("----------------------------");
        txtcodigo.setText("----------------------------");
        btnguardar.setEnabled(false);
         btnguardar1.setEnabled(false);
        pantalla.seleccionado = false;
    }

    public static void evaluar(mAlumno alumno, String rfid) {
        if (alumno == null) {
            txttoken.setText(rfid);
            JOptionPane.showMessageDialog(txttoken, "La tarjeta no esta asignada a ningun alumno, se Puede registrar");
            btnguardar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(txttoken, "La tarjeta esta asignada al alumno: " + alumno.getDatos() + " de la carrera de: " + alumno.getDni());
            btnguardar.setEnabled(false);
        }
    }

    public void llenartabla(mAlumno Alumno) {
        DefaultTableModel modelo = (DefaultTableModel) tablaAlumnos.getModel();
        modelo.addRow(new Object[]{Alumno.getId(), Alumno.getDatos(), Alumno.getRfid(), Alumno.getSaldo()});
        tablaAlumnos.setModel(modelo);
    }

